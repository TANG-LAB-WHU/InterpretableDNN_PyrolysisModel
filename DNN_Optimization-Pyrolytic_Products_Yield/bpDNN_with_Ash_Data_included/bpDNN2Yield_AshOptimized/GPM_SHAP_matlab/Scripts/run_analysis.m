% Add model files directory to path
% First remove any existing paths to avoid conflicts
try
    % Use textscan instead of strsplit for compatibility
    pathList = textscan(path, '%s', 'Delimiter', pathsep);
    pathList = pathList{1};
    
    % Check if rootDir is in the path
    if any(contains(pathList, rootDir))
        rmpath(genpath(rootDir));
        disp('Removed existing paths to avoid conflicts');
    end
catch ME
    disp(['Path handling error: ' ME.message]);
end
