    % Replace the zip function call with updated paths
    zipFilePath = fullfile(trainingDir, 'Result_needed.zip');
    zipFilePath = strrep(zipFilePath, '\', '/');
    
    modifiedContent = strrep(modifiedContent, 'zip(''Result_needed.zip'', {''Results_trained.mat'',''Figures'', ''bpDNN4PyroProd.m''});', ...
        ['zip(''' zipFilePath ''', {''' saveFilePath ''', ''' figDirPath ''', ''' strrep(originalScript, '\', '/') '''});']);
    
    % Write the modified script to a temporary file
    fid = fopen(tempScript, 'w');
    if fid == -1
        error('Could not create temporary file: %s', tempScript);
    end
    fwrite(fid, modifiedContent);
    fclose(fid);
    
    % Store the temp script name in a local variable that we'll use immediately
    tempScriptName = 'bpDNN4PyroProd_temp';
    
    % Check and delete existing temporary files if already present
    tempGlobalsFile = fullfile(modelFilesDir, 'temp_globals.mat');
    if exist(tempGlobalsFile, 'file')
        delete(tempGlobalsFile);
        disp('Removed existing temporary globals file');
