% Load the trained model data with better error checking
trainedDataFile = fullfile(trainingDir, 'Results_trained.mat');
trainedDataFile = strrep(trainedDataFile, '\', '/'); % Use forward slashes for consistency
if exist(trainedDataFile, 'file')
    try
        % Load with specific variable names to ensure they're all available
        trainVars = load(trainedDataFile);
        
        % Check for required variables and transfer to workspace
        requiredVars = {'net', 'input', 'target'};
        missingVars = setdiff(requiredVars, fieldnames(trainVars));
        
        if ~isempty(missingVars)
            disp(['Missing required variables in trained model data: ' strjoin(missingVars, ', ')]);
        end
        
        % Transfer variables to workspace
        fieldNames = fieldnames(trainVars);
        for i = 1:length(fieldNames)
            fieldName = fieldNames{i};
            assignin('base', fieldName, trainVars.(fieldName));
        end
        
        % Special handling for global variables
        global PS TS;
        if isfield(trainVars, 'PS_global') && isfield(trainVars, 'TS_global')
            PS = trainVars.PS_global;
            TS = trainVars.TS_global;
