%% Main Analysis Script for Model Training and SHAP Analysis
% This script runs the pyrolysis model training and then performs SHAP analysis
% on the trained model to explain feature importance and prediction explanations

% Clear workspace and command window
clc; clear all; close all;

% Set warning preferences - disable warnings for numerical precision in nchoosek
warning('off', 'MATLAB:nchoosek:LargeCoefficient');
% Turn off potential parallel pool warnings
warning('off', 'parallel:pctexample:parallelize');
% Turn off path warnings for removing non-existent paths
warning('off', 'MATLAB:rmpath:DirNotFound');
% Turn off mkdir warnings for creating existing directories
warning('off', 'MATLAB:MKDIR:DirectoryExists');
% Turn off strsplit deprecated warning
warning('off', 'MATLAB:string:functionsToBeRemoved');

% Check MATLAB version for compatibility adjustments
matlabVersion = version;
isOlderMatlab = verLessThan('matlab', '9.1'); % R2016b or earlier
if isOlderMatlab
    disp(['Note: Running on MATLAB version ' matlabVersion ' - adjusting for compatibility']);
else
    disp(['MATLAB version: ' matlabVersion]);
end

% Set up paths - Fix path settings
% First get the current script directory
scriptPath = mfilename('fullpath');
[scriptDir, ~, ~] = fileparts(scriptPath);
% Get project root directory (up one level)
rootDir = fileparts(fileparts(scriptDir));
% Set model files and results directories correctly
modelFilesDir = fullfile(rootDir, 'bpDNN4PyroProd_modelfiles');
resultsDir = fullfile(rootDir, 'GPM_SHAP_matlab', 'Results');

% Set up subdirectories for better organization
trainingDir = fullfile(resultsDir, 'Training_Debug');
shapDir = fullfile(resultsDir, 'SHAP_Analysis_Debug');

% Improved path handling - check if path exists before removing to avoid warnings
try
    % Use textscan instead of strsplit for compatibility
    pathList = textscan(path, '%s', 'Delimiter', pathsep);
    pathList = pathList{1};
    
    % Check if rootDir is in the path
    if any(contains(pathList, rootDir))
        rmpath(genpath(rootDir));
        disp('Removed existing paths to avoid conflicts');
    end
catch ME
    disp(['Path handling error: ' ME.message]);
end

% Then add the model files directory to the MATLAB path
addpath(genpath(modelFilesDir));
addpath(genpath(scriptDir)); % Add scripts directory to path

% Check if required functions exist
requiredFunctions = {'nncreate', 'nnpredict', 'nnpreprocess', 'nntrain', 'nneval'};
missingFunctions = {};
for i = 1:length(requiredFunctions)
    if ~exist(requiredFunctions{i}, 'file')
        missingFunctions{end+1} = requiredFunctions{i};
    end
end

if ~isempty(missingFunctions)
    error('Missing required functions: %s. Please ensure these files are in bpDNN4PyroProd_modelfiles and the directory is in the MATLAB path.', strjoin(missingFunctions, ', '));
end

% Create results directories if they don't exist
if ~exist(resultsDir, 'dir')
    mkdir(resultsDir);
end
if ~exist(trainingDir, 'dir')
    mkdir(trainingDir);
end
if ~exist(shapDir, 'dir')
    mkdir(shapDir);
end

%% Step 1: Run the model training script with modified epoch number
disp('Starting model training with reduced epochs (60) for faster debugging...');
disp(['Model files directory: ' modelFilesDir]);
disp(['Results will be saved in: ' trainingDir]);

% Save current directory
originalDir = pwd;

% Switch to model files directory
cd(modelFilesDir);

% Create a temporary copy of the model training script with reduced epochs
originalScript = fullfile(modelFilesDir, 'bpDNN4PyroProd.m');
originalScript = strrep(originalScript, '\', '/'); % Use forward slashes for consistency
tempScript = fullfile(modelFilesDir, 'bpDNN4PyroProd_temp.m');
tempScript = strrep(tempScript, '\', '/'); % Use forward slashes for consistency

% Check if original script exists
if ~exist(originalScript, 'file')
    error('Original script not found: %s', originalScript);
end

try
    % Read the original script
    fid = fopen(originalScript, 'r');
    if fid == -1
        error('Could not open file: %s', originalScript);
    end
    scriptContent = fread(fid, '*char')';
    fclose(fid);
    
    % Replace the epoch number (6000) with 60
    modifiedContent = strrep(scriptContent, 'net.trainParam.epoch = 6000;', 'net.trainParam.epoch = 60;');
    
    % Comment out both "clear all" commands to prevent variable clearing
    modifiedContent = strrep(modifiedContent, 'clc;clear all', 'clc;% clear all  % Commented out to preserve variables');
    modifiedContent = strrep(modifiedContent, 'clear all', '% clear all  % Commented out to preserve variables');
    
    % Add code to store global variables in the workspace
    modifiedContent = strrep(modifiedContent, 'global PS TS;', ...
        ['global PS TS;' newline ...
         'fprintf(''Ensuring global preprocessing variables are captured\n'');' newline ...
         'evalin(''base'', ''global PS TS;'');' newline ... % Ensure globals are visible in base workspace
         'if exist(''PS'', ''var'') && ~isempty(PS)' newline ... % Check if PS exists and is not empty
         '    PS_global = PS;' newline ...
         '    assignin(''base'', ''PS_global'', PS);' newline ... 
         'end' newline ...
         'if exist(''TS'', ''var'') && ~isempty(TS)' newline ... % Check if TS exists and is not empty
         '    TS_global = TS;' newline ...
         '    assignin(''base'', ''TS_global'', TS);' newline ...
         'end']);
    
    % Replace the save locations to use our training directory with proper path handling
    figDirPath = fullfile(trainingDir, 'Figures');
    figDirPath = strrep(figDirPath, '\', '/'); % Use forward slashes for consistency
    
    modifiedContent = strrep(modifiedContent, 'mkdir(''Figures'');', ...
        ['if ~exist(''' figDirPath ''', ''dir'')' newline ...
         '    mkdir(''' figDirPath ''');' newline ...
         'end']);
    
    modifiedContent = strrep(modifiedContent, 'FolderName = ''Figures'';', ['FolderName = ''' figDirPath ''';']);
    
    % Modified save command to explicitly ensure PS_global and TS_global are saved
    saveFilePath = fullfile(trainingDir, 'Results_trained.mat');
    saveFilePath = strrep(saveFilePath, '\', '/'); % Use forward slashes for consistency
    
    saveCmd = ['% Create PS_global and TS_global if they don''t exist' newline ...
               'global PS TS;' newline ...
               'if ~exist(''PS_global'', ''var'') && exist(''PS'', ''var'') && ~isempty(PS)' newline ...
               '    PS_global = PS;' newline ...
               '    fprintf(''Created PS_global from PS\n'');' newline ...
               'end' newline ...
               'if ~exist(''TS_global'', ''var'') && exist(''TS'', ''var'') && ~isempty(TS)' newline ...
               '    TS_global = TS;' newline ...
               '    fprintf(''Created TS_global from TS\n'');' newline ...
               'end' newline ...
               '% Check which variables exist before saving' newline ...
               'varsToSave = {};' newline ...
               'varNames = {''net'', ''input'', ''target'', ''trainInp'', ''trainTarg'', ''trainOut'', ' ...
               '''valInp'', ''valTarg'', ''valOut'', ''testInp'', ''testTarg'', ''testOut'', ' ...
               '''tr'', ''PS_global'', ''TS_global''};' newline ...
               'for i = 1:length(varNames)' newline ...
               '    if exist(varNames{i}, ''var'')' newline ...
               '        varsToSave{end+1} = varNames{i};' newline ...
               '        fprintf(''Adding %s to save list\n'', varNames{i});' newline ...
